/** Map IP geolocation country code → app language */
export function countryToLang(countryCode) {
  if (!countryCode) return "en";
  const cc = countryCode.toUpperCase();
  if (cc === "ID") return "id";
  if (cc === "JP") return "ja";
  return "en";
}
